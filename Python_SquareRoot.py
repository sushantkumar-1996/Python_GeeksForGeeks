"""Python Program to calculate the square root of a number entered by the user"""

num = float(input("Enter a Number"))
num_sqrt  = num ** 0.5  # ** operator is the exponentiation operator it is same as num raised to power 1/2 or 0.5
print("The Square root is :", num_sqrt)
cube_root = num ** (1 / 3)
print("The Cube root is", cube_root)