"""Python program for converting decimal numbers into binary , octal and hexadecimal using inbuilt functions bin(), oct(),and hex()
These functions take a integer as input and returns a string"""

n1 = int(input("Enter a Decimal number"))

print("binary value is:", bin(n1))
print("Octal value is:", oct(n1))
print("hexadecimal value is", hex(n1))
